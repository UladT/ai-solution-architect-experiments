"""
Prompt Artifact Saver Module
Handles saving prompts and prompt evolution reports to disk.
Provides utilities for documenting prompt engineering process.
"""

import os
from datetime import datetime


def save_prompt_artifact(
    prompt_text: str,
    prompt_name: str,
    version: str,
    scenario_name: str = "",
    results_dir: str = "results"
) -> str:
    """
    Save a prompt to a dedicated file for review and documentation.
    
    Args:
        prompt_text: The prompt content
        prompt_name: Name of the prompt (e.g., "react_template", "poor_v0", "improved_v1")
        version: Version identifier
        scenario_name: Optional scenario name prefix
        results_dir: Directory to save to
        
    Returns:
        Path to saved file
    """
    os.makedirs(results_dir, exist_ok=True)
    
    # Build filename
    safe_scenario = scenario_name.replace(" ", "_") if scenario_name else "general"
    timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
    filename = (
        f"{results_dir}/"
        f"prompt_{safe_scenario}_{prompt_name}_v{version}_{timestamp}.txt"
    )
    
    with open(filename, "w", encoding="utf-8") as f:
        f.write(f"Prompt: {prompt_name}\n")
        f.write(f"Version: {version}\n")
        f.write(f"Scenario: {scenario_name}\n")
        f.write(f"Timestamp: {datetime.now().isoformat()}\n")
        f.write("=" * 70 + "\n\n")
        f.write(prompt_text)
    
    return filename


def save_prompt_evolution(
    poor_prompt: str,
    improved_prompt: str,
    meta_analysis: dict,
    scenario_name: str = "",
    results_dir: str = "results"
) -> str:
    """
    Save a comprehensive prompt evolution document showing the progression.
    
    Args:
        poor_prompt: Original v0.1 prompt
        improved_prompt: Enhanced v1.0 prompt
        meta_analysis: Meta-prompting analysis
        scenario_name: Optional scenario name
        results_dir: Directory to save to
        
    Returns:
        Path to saved file
    """
    os.makedirs(results_dir, exist_ok=True)
    
    safe_scenario = scenario_name.replace(" ", "_") if scenario_name else "general"
    timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
    filename = f"{results_dir}/prompt_evolution_{safe_scenario}_{timestamp}.md"
    
    content = f"""# Prompt Evolution Report
Generated: {datetime.now().isoformat()}
Scenario: {scenario_name}

## Executive Summary

This document shows the progression of prompt engineering for {scenario_name or 'this scenario'}.

---

## V0.1: Poor Prompt (Baseline)

```
{poor_prompt}
```

### Issues Identified (by LLM):

"""
    
    if meta_analysis.get("weaknesses"):
        for i, weakness in enumerate(meta_analysis["weaknesses"], 1):
            content += f"- **Issue {i}**: {weakness}\n"
    
    content += f"""
### Quality Score: {meta_analysis.get('quality_score', 'N/A')}/10

**Justification**: {meta_analysis.get('quality_justification', 'N/A')}

---

## V1.0: Improved Prompt (Enhanced)

```
{improved_prompt}
```

### Improvements Applied:

"""
    
    if meta_analysis.get("improvements"):
        for i, improvement in enumerate(meta_analysis["improvements"], 1):
            content += f"- **Improvement {i}**: {improvement}\n"
    
    content += f"""
### Missing Elements for Production:

"""
    
    if meta_analysis.get("missing_elements"):
        for i, element in enumerate(meta_analysis["missing_elements"], 1):
            content += f"- {element}\n"
    
    content += f"""
---

## Recommended Enhanced Section

```
{meta_analysis.get('improved_section', 'N/A')}
```

---

## Meta-Analysis Metrics

| Metric | Value |
|--------|-------|
| Quality Score | {meta_analysis.get('quality_score', 'N/A')}/10 |
| Weaknesses Identified | {len(meta_analysis.get('weaknesses', []))} |
| Improvements Suggested | {len(meta_analysis.get('improvements', []))} |
| Missing Elements | {len(meta_analysis.get('missing_elements', []))} |

---

Generated by Prompt Engineering Task (AC-1: Meta-Prompting)
"""
    
    with open(filename, "w", encoding="utf-8") as f:
        f.write(content)
    
    return filename
